#include "fillit.h"

void	find_first(char grid[4][5], int *i, int *j, char c)
{
	*j = -1;
	while (++*j < 4)
	{
		*i = -1;
		while (++*i < 4)
			if (grid[*j][*i] == c)
				return ;
	}
}
